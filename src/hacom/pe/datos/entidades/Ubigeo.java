package hacom.pe.datos.entidades;

public class Ubigeo {

	private int idDepa;
	private int deparment_id;
	private int province_id;
	private String name;
	private int id;
	private int mefid;
	private int mefprov;
	private String nameprov; 
	public int getIdDepa() {
		return idDepa;
	}
	public void setIdDepa(int idDepa) {
		this.idDepa = idDepa;
	}
	public int getDeparment_id() {
		return deparment_id;
	}
	public void setDeparment_id(int deparment_id) {
		this.deparment_id = deparment_id;
	}
	public int getProvince_id() {
		return province_id;
	}
	public void setProvince_id(int province_id) {
		this.province_id = province_id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getMefid() {
		return mefid;
	}
	public void setMefid(int mefid) {
		this.mefid = mefid;
	}
	public int getMefprov() {
		return mefprov;
	}
	public void setMefprov(int mefprov) {
		this.mefprov = mefprov;
	}
	public String getNameprov() {
		return nameprov;
	}
	public void setNameprov(String nameprov) {
		this.nameprov = nameprov;
	}
	
	
}
