package hacom.pe.datos.entidades;

public class Sectorista {

	private  int idSec;
	private String nombreSec;
	private String apellidoSec;
	private String usuSec;
	private String contSec;
	private String estado;
	private String dni;
	private String tipo;
	public Sectorista() {
		super();
	}
	public int getIdSec() {
		return idSec;
	}
	public void setIdSec(int idSec) {
		this.idSec = idSec;
	}
	public String getNombreSec() {
		return nombreSec;
	}
	public void setNombreSec(String nombreSec) {
		this.nombreSec = nombreSec;
	}
	public String getApellidoSec() {
		return apellidoSec;
	}
	public void setApellidoSec(String apellidoSec) {
		this.apellidoSec = apellidoSec;
	}
	public String getUsuSec() {
		return usuSec;
	}
	public void setUsuSec(String usuSec) {
		this.usuSec = usuSec;
	}
	public String getContSec() {
		return contSec;
	}
	public void setContSec(String contSec) {
		this.contSec = contSec;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	public String getDni() {
		return dni;
	}
	public void setDni(String dni) {
		this.dni = dni;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	
	
	
}
